package com.example.abyssaldescent.dungeon;

/**
 * Типы комнат подземелья Эфрокар.
 * Каждый тип определяет содержимое и поведение комнаты.
 */
public enum RoomType {

    /** Боевая комната — содержит врагов, двери закрыты до зачистки */
    COMBAT,

    /** Сокровищница — содержит сундук с предметом */
    TREASURE,

    /** Комната перехода — содержит лестницу на следующий этаж */
    TRANSITION,

    /** Стартовая комната — без врагов, точка появления героя */
    START,

    /** Алтарь Памяти — можно восстановить HP или усилить оружие */
    ALTAR
}